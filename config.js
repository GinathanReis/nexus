function abrirAbaConfig() {
    var abaConfig = document.getElementById("abaConfig");
    abaConfig.style.display = "block";
}

// Função para fechar a aba Config
function fecharAbaConfig() {
    var abaConfig = document.getElementById("abaConfig");
    abaConfig.style.display = "none";
}

